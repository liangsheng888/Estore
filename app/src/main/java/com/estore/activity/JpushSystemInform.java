            content = bundle.getString(JPushInterface.EXTRA_ALERT);
            Log.i("cc", "----onReceive: "+content);
            //System.out.println("需要传过去的Id"+userId);
            RequestParams requestParams=new RequestParams(HttpUrlUtils.HTTP_URL+"addmessageservlet");
            requestParams.addBodyParameter("title",title);
            requestParams.addBodyParameter("message",content);

            Log.i("cc","=======title======="+title);
            Log.i("cc", "----onReceive: "+content);
